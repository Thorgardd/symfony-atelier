<?php

namespace App\Entity;

use App\Repository\CarRepository;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: CarRepository::class)]
class Car
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 255)]
    private ?string $brand = null;

    #[ORM\Column(length: 255)]
    private ?string $model = null;

    #[ORM\Column(length: 255)]
    private ?string $matricule = null;

    #[ORM\OneToOne(mappedBy: 'vehicule', cascade: ['persist', 'remove'])]
    private ?Intervention $vehicule = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getBrand(): ?string
    {
        return $this->brand;
    }

    public function setBrand(string $brand): self
    {
        $this->brand = $brand;

        return $this;
    }

    public function getModel(): ?string
    {
        return $this->model;
    }

    public function setModel(string $model): self
    {
        $this->model = $model;

        return $this;
    }

    public function getMatricule(): ?string
    {
        return $this->matricule;
    }

    public function setMatricule(string $matricule): self
    {
        $this->matricule = $matricule;

        return $this;
    }

    public function getVehicule(): ?Intervention
    {
        return $this->vehicule;
    }

    public function setVehicule(Intervention $vehicule): self
    {
        // set the owning side of the relation if necessary
        if ($vehicule->getVehicule() !== $this) {
            $vehicule->setVehicule($this);
        }

        $this->vehicule = $vehicule;

        return $this;
    }
}
